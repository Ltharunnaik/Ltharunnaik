package sample.java;

public class SwitchCondition {

	public static void main(String[] args) {
		
		int i = 4;
		switch(i) {
		case 1:
			System.out.println("welcome to degree exam ");
			break;
		case 2:
			System.out.println("first exam is english on the 01-01-2024");
			break;
		case 3:
			System.out.println("exam is post pone that the day");
			break;
		case 4:
			System.out.println("all exam are completed");
			break;
		case 5:
			System.out.println("exam result on the today");
			break;
		default:
			System.out.println("all exam are faild");
			break;
		}

	}

}
